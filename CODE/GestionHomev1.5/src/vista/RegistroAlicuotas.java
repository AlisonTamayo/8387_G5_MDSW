    private void mostrardatos(String String) throws ClassNotFoundException, SQLException {
        com.mysql.jdbc.Connection conex;
          Class.forName("com.mysql.jdbc.Driver");
          
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Numero de Casa");
        modelo.addColumn("Valos de Pago");
        modelo.addColumn("Descripci처n");
        TableAlicuotas.setModel(modelo);
        String consultasql = "select*from alicuota";
        String data[] = new String[5];
        Statement st;

        try {
            conex=(com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/registroalicuota","root","");
            System.out.println("Conectado");
            st = conex.createStatement();
            ResultSet rs = st.executeQuery(consultasql);
            while (rs.next()) {
                data[0] = rs.getString(1);
                data[1] = rs.getString(2);
                data[2] = rs.getString(3);
                data[3] = rs.getString(4);
                data[4] = rs.getString(5);
                modelo.addRow(data);

            }
        } catch (SQLException e) {
            System.out.println("Error al mostrar Datos " + e);
        }

    }
