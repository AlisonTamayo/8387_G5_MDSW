    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtCedula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingrese nombre", "Mensaje", JOptionPane.WARNING_MESSAGE);
                return;
            }

            LogicaNegocio controladorPago = new LogicaNegocio();
            cliente = controladorPago.buscarCliente(txtCedula.getText());

            if (cliente != null) {

                txtNombre.setText(cliente.getNombre_apellido());
                txtNumeroCasa.setText(cliente.getNumeroCasa());
                

                //listarFacturas(controladorPago);

            } else {
                JOptionPane.showMessageDialog(null, "Cliente no existe", "Mensaje", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al ejecutar select", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

